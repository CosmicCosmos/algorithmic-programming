    public static void rec(String digits, ArrayList<String> al, HashMap<Integer, String> hm, int cnt, String nowString) {
        if (cnt == digits.length()) {
            al.add(nowString);
            return;
        }
        char[] chars = hm.get(digits.charAt(cnt) - '0').toCharArray();
        for (char x : chars) {
            nowString += x;
            rec(digits, al, hm, cnt + 1, nowString);
            nowString = nowString.substring(0, nowString.length() - 1);
        }
    }
    
