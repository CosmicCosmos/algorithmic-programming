    public static ArrayList<Integer> prevSmaller(ArrayList<Integer> arr) {
        Stack<Integer> st = new Stack<>();
        for (int i : arr) {
            st.push(i);
        }
        st.pop();
        ArrayList<Integer> ans = new ArrayList<>();
        for (int i = 0; i < arr.size(); i++) {
            ans.add(-1);
        }
        for (int i = arr.size() - 1; i >= 0; i--) {
            if (!st.isEmpty()) {
                int curr = st.pop();
                if (curr >= arr.get(i)) {
                    Stack<Integer> putAgain = new Stack<>();
                    while (!st.isEmpty() && st.peek() >= arr.get(i)) {
                        putAgain.push(st.pop());
                    }
                    if (!st.isEmpty()) {
                        curr = st.pop();
                        ans.set(i, curr);
                        putAgain.push(curr);
                    }
                    while (!putAgain.isEmpty()) {
                        st.push(putAgain.pop());
                    }
                } else {
                    ans.set(i ,curr);
                }
            }
        }
//        System.out.println(ans);
        return ans;
    }
    
